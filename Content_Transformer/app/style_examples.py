def embed_style_examples():
    """Simple function to embed all style examples into RAG"""
    from rag.vector_store import add_style_guide
    
    for example in STYLE_EXAMPLES:
        metadata = {
            "style": example["style"],
            "tone": example["tone"], 
            "type": example["type"]
        }
        
        add_style_guide(example["text"], metadata)
        print(f"Embedded: {example['style']} - {example['tone']} - {example['type']}")

def ingest_external_format(file_path, style, tone, content_type):
    """Simple function to ingest external format files"""
    import os
    
    if not os.path.exists(file_path):
        print(f"File not found: {file_path}")
        return
    
    with open(file_path, 'r', encoding='utf-8') as file:
        content = file.read()
    
    metadata = {
        "style": style,
        "tone": tone,
        "type": content_type,
        "source": file_path
    }
    
    from rag.vector_store import add_style_guide
    add_style_guide(content, metadata)
    print(f"Embedded external file: {file_path}")

if __name__ == "__main__":
    embed_style_examples() 